#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const YAML = require('yaml');

// Simple YAML parser for our specific format
function parseYaml(content) {
  const data = YAML.parse(content);
  const workoutTypes = [];
  const workoutSubTypes = {};
  let currentType = null;

  for (const workoutType of Object.keys(data.workout_types)) {
    const sport = data.workout_types[workoutType];
    workoutTypes.push({
      key: workoutType,
      pace: false,
      location: false,
      distance: false,
      repetition: false,
      weight: false,
      duration: false,
      ...sport,
    });

    for (const subSport of Object.keys(sport.sub_types)) {
      workoutSubTypes[subSport] = sport.sub_types[subSport].name;
    }
  }

  if (currentType) {
    workoutTypes.push(currentType);
  }

  return {
    workoutTypes,
    workoutSubTypes,
  };
}

// Read the YAML file
const yamlPath = path.join(__dirname, '..', '..', 'workout-types.yaml');
const yamlContent = fs.readFileSync(yamlPath, 'utf8');
const { workoutTypes, workoutSubTypes } = parseYaml(yamlContent);

// Generate TypeScript code
const tsCode = `// Code generated by generate-workout-types.js; DO NOT EDIT.
import { _ } from "@ngx-translate/core";

/**
 * Workout types and configuration
 */

export interface WorkoutTypeConfig {
  value: string;
  name: string;
  location: boolean;
  distance: boolean;
  repetition: boolean;
  weight: boolean;
  duration: boolean;
  pace: boolean;
}

export const WORKOUT_TYPES: WorkoutTypeConfig[] = [
${workoutTypes
  .map(
    (wt) => `  {
    value: '${wt.key}',
    name: _('${wt.name}'),
    location: ${wt.location},
    distance: ${wt.distance},
    repetition: ${wt.repetition},
    weight: ${wt.weight},
    duration: true,
    pace: ${wt.pace}
  }`,
  )
  .join(',\n')}
];

export function getWorkoutTypeConfig(type: string): WorkoutTypeConfig | undefined {
  return WORKOUT_TYPES.find(wt => wt.value === type);
}

export const WORKOUT_SUB_TYPES: Record<string, string> = {
${Object.entries(workoutSubTypes)
  .map((wt) => `  ${wt[0]}: _('${wt[1]}')`)
  .join(',\n')}
};
`;

// Write the TypeScript file
const outputPath = path.join(__dirname, '..', 'src', 'app', 'core', 'types', 'workout-types.ts');
fs.writeFileSync(outputPath, tsCode, 'utf8');

console.log('Generated workout-types.ts successfully');
